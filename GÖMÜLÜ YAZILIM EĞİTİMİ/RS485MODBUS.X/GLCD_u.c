void glcd_write_data (unsigned int LCD_data,unsigned int chip)     
{        
    GLCD_DI_SetHigh();      
    
    if(chip)
        GLCD_CS1_SetHigh();
    else
        GLCD_CS2_SetHigh();
    
    if(LCD_data&(0b00000001))    LED1_SetHigh();  else    LED1_SetLow();
    if(LCD_data&(0b00000010))    LED2_SetHigh();  else    LED2_SetLow();
    if(LCD_data&(0b00000100))    LED3_SetHigh();  else    LED3_SetLow();
    if(LCD_data&(0b00001000))    LED4_SetHigh();  else    LED4_SetLow();
    if(LCD_data&(0b00010000))    LED5_SetHigh();  else    LED5_SetLow();
    if(LCD_data&(0b00100000))    LED6_SetHigh();  else    LED6_SetLow();
    if(LCD_data&(0b01000000))    LED7_SetHigh();  else    LED7_SetLow();
    if(LCD_data&(0b10000000))    LED8_SetHigh();  else    LED8_SetLow();
       
    DELAY_microseconds(1);
    
    GLCD_E_SetHigh(); 
    DELAY_microseconds(1);  //delay_usg(1);
    GLCD_E_SetLow();
    
    GLCD_CS1_SetLow();
    GLCD_CS2_SetLow();   
}

